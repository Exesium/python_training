class Contact:
    def __init__(self, firstname, lastname, nickname, title, company, address, mobile, work, email):
        self.firstname = firstname
        self.lastname = lastname
        self.nickname = nickname
        self.title = title
        self.company = company
        self.address = address
        self.mobile = mobile
        self.work = work
        self.email = email
