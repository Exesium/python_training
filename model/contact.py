# -*- coding: utf-8 -*-


class Contact:

    def __init__(self, firstname, middlename, lastname, nickname, title, company, address, mobile, work, email,
                 b_day, b_month, b_year):
        self.firstname = firstname
        self.middlename = middlename
        self.lastname = lastname
        self.nickname = nickname
        self.title = title
        self.company = company
        self.address = address
        self.mobile = mobile
        self.work = work
        self.email = email
        self.b_day = b_day
        self.b_month = b_month
        self.b_year = b_year
